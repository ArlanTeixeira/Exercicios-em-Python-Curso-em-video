algo = input('Escreva algo')
print('Voçê digitou: {}'.format(algo))
print('O tipo primitivo da sua variável é: {}'.format(type(algo)))
print('Tem espaços: ', algo.isspace())
print('É um numero: ', algo.isnumeric())
print('É alfabético: ', algo.isalpha())
print('É alfanúmerico: ', algo.isalnum())
print('Está em maiúsculo: ', algo.isupper())
print('Está em minúsculo: ', algo.islower())
print('Está em capitalizado: ', algo.istitle())



